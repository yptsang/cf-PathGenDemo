function drawEnvironmentMap(ObsCoor)
% The function is used to generate the environment map
for k1 = 1:size(ObsCoor,1)
    vert = [ObsCoor(k1,2) ObsCoor(k1,4) ObsCoor(k1,5); ObsCoor(k1,1) ObsCoor(k1,4) ObsCoor(k1,5); ObsCoor(k1,1) ObsCoor(k1,3) ObsCoor(k1,5); ObsCoor(k1,2) ObsCoor(k1,3) ObsCoor(k1,5); ... 
            ObsCoor(k1,2) ObsCoor(k1,4) ObsCoor(k1,6); ObsCoor(k1,1) ObsCoor(k1,4) ObsCoor(k1,6); ObsCoor(k1,1) ObsCoor(k1,3) ObsCoor(k1,6); ObsCoor(k1,2) ObsCoor(k1,3) ObsCoor(k1,6)];
    fac = [1 2 3 4; ...
           2 6 7 3; ...
           4 3 7 8; ...
           1 5 8 4; ...
           1 2 6 5; ...
           5 6 7 8];
    patch('Faces',fac,'Vertices',vert,'FaceColor','[0.55,0.71,0.12]','FaceAlpha',0.8);  % patch function
    hold on;
end
    
